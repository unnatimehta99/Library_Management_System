DELIMITER //

CREATE PROCEDURE IssueBook(IN bookID INT, IN studentID INT, IN issueBy INT, IN issueTime VARCHAR(50))
BEGIN
    DECLARE isBookIssued INT;
    
    -- Check if the book is already issued
    SELECT COUNT(*) INTO isBookIssued
    FROM book_issue_log
    WHERE book_id = bookID AND return_time IS NULL;

    IF isBookIssued = 0 THEN
        -- Book is not yet issued, update the book_issue_log table
        INSERT INTO book_issue_log (book_id, student_id, issue_by, issued_at, return_time)
        VALUES (bookID, studentID, issueBy, issueTime, '');

        -- Mark the book as unavailable in the availability table
        UPDATE availibility
        SET available_status = 0
        WHERE book_id = bookID;

        SELECT 1 AS result; -- Success
    ELSE
        SELECT 0 AS result; -- Book is already issued
    END IF;
END //

