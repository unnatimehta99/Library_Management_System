-- Database: `library`
--
CREATE DATABASE IF NOT EXISTS `library` DEFAULT CHARACTER SET latin1 COLLATE latin1_swedish_ci;
USE `library`;

-- --------------------------------------------------------

-- Create the `book_categories` table
CREATE TABLE IF NOT EXISTS `book_categories` (
  `id` INT(5) NOT NULL AUTO_INCREMENT,
  `category` VARCHAR(255) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `branches` table
CREATE TABLE IF NOT EXISTS `branches` (
  `id` INT(5) NOT NULL AUTO_INCREMENT,
  `branch` VARCHAR(255) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `student_categories` table
CREATE TABLE IF NOT EXISTS `student_categories` (
  `cat_id` INT(2) NOT NULL AUTO_INCREMENT,
  `category` VARCHAR(512) NOT NULL,
  `max_allowed` INT(1) NOT NULL,
  PRIMARY KEY (`cat_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `users` table
CREATE TABLE IF NOT EXISTS `users` (
  `id` INT(11) NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(255) NOT NULL,
  `username` VARCHAR(512) NOT NULL,
  `password` VARCHAR(255) NOT NULL,
  `verification_status` TINYINT(1) NOT NULL DEFAULT '0',
  `remember_token` TEXT NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `students` table
CREATE TABLE IF NOT EXISTS `students` (
  `student_id` INT(11) NOT NULL AUTO_INCREMENT,
  `first_name` VARCHAR(512) NOT NULL,
  `last_name` VARCHAR(512) NOT NULL,
  `approved` INT(1) NOT NULL DEFAULT '0',
  `rejected` INT(1) NOT NULL DEFAULT '0',
  `category` INT(1) NOT NULL,
  `roll_num` VARCHAR(15) NOT NULL,
  `branch` INT(1) NOT NULL DEFAULT '0',
  `year` INT(5) NOT NULL,
  `books_issued` INT(1) NOT NULL DEFAULT '0',
  `email_id` VARCHAR(512) NOT NULL,
  PRIMARY KEY (`student_id`),
  FOREIGN KEY (`branch`) REFERENCES `branches`(`id`),
  FOREIGN KEY (`category`) REFERENCES `student_categories`(`cat_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `books` table
CREATE TABLE IF NOT EXISTS `books` (
  `book_id` INT(11) NOT NULL AUTO_INCREMENT,
  `title` VARCHAR(1000) NOT NULL,
  `description` TEXT NOT NULL,
  `category_id` INT(5) NOT NULL,
  `added_by` INT(11) NOT NULL,
  `added_at_timestamp` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`book_id`),
  FOREIGN KEY (`added_by`) REFERENCES `users`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `book_issue_log` table
CREATE TABLE IF NOT EXISTS `book_issue_log` (
  `id` INT(16) NOT NULL AUTO_INCREMENT,
  `book_id` INT(11) NOT NULL,
  `student_id` INT(11) NOT NULL,
  `issue_by` INT(11) NOT NULL,
  `issued_at` VARCHAR(50),
  `return_time` VARCHAR(50) NOT NULL,
  `time_stamp` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  FOREIGN KEY (`book_id`) REFERENCES `books`(`book_id`),
  FOREIGN KEY (`student_id`) REFERENCES `students`(`student_id`),
  FOREIGN KEY (`issue_by`) REFERENCES `users`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `availibility` table
CREATE TABLE IF NOT EXISTS `availibility` (
  `issue_id` INT(11) NOT NULL AUTO_INCREMENT,
  `book_id` INT(11) NOT NULL,
  `available_status` TINYINT(1) NOT NULL DEFAULT '1',
  `added_by` INT(11) NOT NULL,
  `added_at_timestamp` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`issue_id`),
  FOREIGN KEY (`book_id`) REFERENCES `books`(`book_id`),
  FOREIGN KEY (`added_by`) REFERENCES `users`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `authors` table
CREATE TABLE IF NOT EXISTS `authors` (
  `author_id` int(11) NOT NULL AUTO_INCREMENT,
  `author_name` varchar(255) NOT NULL,
  PRIMARY KEY (`author_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `book_authors` table
CREATE TABLE IF NOT EXISTS `book_authors` (
  `book_id` int(11) NOT NULL,
  `author_id` int(11) NOT NULL,
  PRIMARY KEY (`book_id`, `author_id`),
  FOREIGN KEY (`book_id`) REFERENCES `books`(`book_id`),
  FOREIGN KEY (`author_id`) REFERENCES `authors`(`author_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

-- Create the `book_reviews` table
CREATE TABLE IF NOT EXISTS `book_reviews` (
  `review_id` int(11) NOT NULL AUTO_INCREMENT,
  `book_id` int(11) NOT NULL,
  `review_text` text NOT NULL,
  `rating` int(1) NOT NULL,
  `reviewed_by` INT NOT NULL,
  `reviewed_at` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`review_id`),
  FOREIGN KEY (`book_id`) REFERENCES `books`(`book_id`),
  FOREIGN KEY (`reviewed_by`) REFERENCES `students`(`student_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE IF NOT EXISTS `book_publishers` (
  `publisher_id` int(11) NOT NULL AUTO_INCREMENT,
  `publisher_name` varchar(255) NOT NULL,
  PRIMARY KEY (`publisher_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE IF NOT EXISTS `book_publisher_relationship` (
  `book_id` int(11) NOT NULL,
  `publisher_id` int(11) NOT NULL,
  PRIMARY KEY (`book_id`, `publisher_id`),
  FOREIGN KEY (`book_id`) REFERENCES `books`(`book_id`),
  FOREIGN KEY (`publisher_id`) REFERENCES `book_publishers`(`publisher_id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

