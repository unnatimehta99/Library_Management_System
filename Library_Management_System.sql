-- Rank books by their review counts:

SELECT 
    b.title, 
    COUNT(br.review_id) AS review_count,
    DENSE_RANK() OVER (ORDER BY COUNT(br.review_id) DESC) AS review_rank
FROM books b
LEFT JOIN book_reviews br ON b.book_id = br.book_id
GROUP BY b.book_id
ORDER BY review_count DESC;

-- Calculate the average rating for each book:

SELECT 
    b.title,
    AVG(br.rating) AS avg_rating,
    DENSE_RANK() OVER (ORDER BY AVG(br.rating) DESC) AS rating_rank
FROM books b
LEFT JOIN book_reviews br ON b.book_id = br.book_id
GROUP BY b.book_id
ORDER BY avg_rating DESC;

-- List students with the highest number of books issued:

SELECT 
    CONCAT(s.first_name, ' ', s.last_name) AS student_name,
    s.books_issued,
    DENSE_RANK() OVER (ORDER BY s.books_issued DESC) AS issuance_rank
FROM students s
ORDER BY s.books_issued DESC;

-- Retrieve the top 3 authors based on the number of books they've written:

SELECT 
    a.author_name,
    COUNT(DISTINCT ba.book_id) AS book_count,
    RANK() OVER (ORDER BY COUNT(DISTINCT ba.book_id) DESC) AS author_rank
FROM authors a
INNER JOIN book_authors ba ON a.author_id = ba.author_id
GROUP BY a.author_id
ORDER BY book_count DESC
LIMIT 3;

-- Display the total count of books published by each publisher:

SELECT 
    bp.publisher_name,
    COUNT(DISTINCT br.book_id) AS total_books_published,
    ROW_NUMBER() OVER (ORDER BY COUNT(DISTINCT br.book_id) DESC) AS publisher_rank
FROM book_publishers bp
INNER JOIN book_publisher_relationship bpr ON bp.publisher_id = bpr.publisher_id
INNER JOIN books br ON bpr.book_id = br.book_id
GROUP BY bp.publisher_id
ORDER BY total_books_published DESC;

-- Identifying Maximum Books Issued in a Year by Branch:

SELECT branch, year, MAX(issued_count) AS max_books_issued
FROM (
    SELECT s.branch, s.year, COUNT(*) AS issued_count,
           RANK() OVER (PARTITION BY s.branch, s.year ORDER BY COUNT(*) DESC) AS yearly_rank
    FROM students s
    JOIN book_issue_log bi ON s.student_id = bi.student_id
    GROUP BY s.branch, s.year
) AS yearly_stats
WHERE yearly_rank = 1
GROUP BY branch, year;

